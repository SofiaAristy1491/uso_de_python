'''
Ejercicio 1: Operadores Aritméticos

Problema: Escribe un programa que solicite al usuario dos números y 
realice las operaciones de suma, resta, multiplicación, división y módulo.

'''

numero_1=float(input("ingrese un número"))
numero_2=float(input("ingrese un número 2"))

#suma
print("el resultado de la suma: ", numero_1 + numero_2)

#resta
print("el resultado de la resta: ",numero_1 - numero_2)

#Multiplicación
print("el resultado de la multiplicación: ",numero_1 * numero_2)

#División
print("el resultado de la división: ",numero_1 / numero_2)

#Módulo
print("el resultado de la módulo ",numero_1 % numero_2)
